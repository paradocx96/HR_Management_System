
function social(data){
  if(data == 'facebook'){
    alert("Now you going to visit our Facebook fan page.");
    window.location.href = 'http://www.facebook.com';
  }
  else if(data == 'twitter'){
    alert("Now you going to visit our Twitter fan page.");
    window.location.href = 'http://www.twitter.com';
  }
  else if(data == 'instagram'){
    alert("Now you going to visit our Instagram fan page.");
    window.location.href = 'http://www.instagram.com';
  }
  else if(data == 'linkedin'){
    alert("Now you going to visit our LinkedIn fan page.");
    window.location.href = 'http://www.linkedin.com';
  }
}
